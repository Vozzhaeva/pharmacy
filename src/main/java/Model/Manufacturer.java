package Model;

public class Manufacturer {
    private Integer id_m;
    private String name_m;
    private String address_m;
    private String phone_m;
    private String email_m;

    public Manufacturer() {
    }

    public Integer getId_m() {
        return id_m;
    }

    public void setId_m(Integer id_m) {
        this.id_m = id_m;
    }

    public String getName_m() {
        return name_m;
    }

    public void setName_m(String name_m) {
        this.name_m = name_m;
    }

    public String getAddress_m() {
        return address_m;
    }

    public void setAddress_m(String address_m) {
        this.address_m = address_m;
    }

    public String getPhone_m() {
        return phone_m;
    }

    public void setPhone_m(String phone_m) {
        this.phone_m = phone_m;
    }

    public String getEmail_m() {
        return email_m;
    }

    public void setEmail_m(String email_m) {
        this.email_m = email_m;
    }
}
